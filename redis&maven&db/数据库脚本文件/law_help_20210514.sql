    ANS_TEXT VARCHAR(3072)    COMMENT '答案文本' ,
    CLICK_TIMES INT    COMMENT '点击次数' ,
    UP_TIME VARCHAR(36)    COMMENT '上传时间' ,
    UP_ID VARCHAR(36)    COMMENT '上传者ID' ,
    IF_DELETE INT    COMMENT '是否失效' ,
    PRIMARY KEY (ANS_ID)
) COMMENT = '问题答案表 ';

ALTER TABLE QUE_ANSWER COMMENT '问题答案表';
