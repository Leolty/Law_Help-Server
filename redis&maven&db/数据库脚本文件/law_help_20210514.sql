DROP TABLE USER;
CREATE TABLE USER(
    USER_ID VARCHAR(36) NOT NULL   COMMENT '主键ID' ,
    USER_NAME VARCHAR(36)    COMMENT '用户昵称' ,
    USER_AVATAR VARCHAR(255)    COMMENT '用户头像' ,
    USER_GENDER INT    COMMENT '用户性别' ,
    USER_TIME VARCHAR(36)    COMMENT '注册时间' ,
    USER_TYPE INT    COMMENT '用户类型 0普通;1运维;2管理;3开发' ,
    USER_SESSION VARCHAR(36)    COMMENT '登陆状态校验码' ,
    PRIMARY KEY (USER_ID)
) COMMENT = '用户表 ';

ALTER TABLE USER COMMENT '用户表';
DROP TABLE QUESTION;
CREATE TABLE QUESTION(
    QUE_ID VARCHAR(36) NOT NULL   COMMENT '主键ID' ,
    QUE_FATHER_ID VARCHAR(36)    COMMENT '父问题ID' ,
    QUE_OPTION VARCHAR(36)    COMMENT '父问题的选项' ,
    QUE_TEXT VARCHAR(255)    COMMENT '问题内容' ,
    QUE_IF_LEAF INT    COMMENT '问题层次 0root1node2leaf' ,
    QUE_HAS_OTHER INT    COMMENT '是否允许其他 0、1' ,
    CLICK_TIMES INT    COMMENT '点击次数' ,
    UP_TIME VARCHAR(36)    COMMENT '上传时间' ,
    UP_ID VARCHAR(36)    COMMENT '上传者' ,
    IF_DELETE INT    COMMENT '是否删除 0、1' ,
    PRIMARY KEY (QUE_ID)
) COMMENT = '问题表 ';

ALTER TABLE QUESTION COMMENT '问题表';
DROP TABLE QUE_ANSWER;
