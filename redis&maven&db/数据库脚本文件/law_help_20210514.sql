    ANSWER_TIME VARCHAR(36)    COMMENT '回答时间' ,
    ANSWER_USER_ID VARCHAR(36)    COMMENT '回答者' ,
    ANSWER_VERSION INT    COMMENT '提问类型 0审核用；1正式用' ,
