    ASK_ID VARCHAR(36) NOT NULL   COMMENT '主键ID' ,
    QUE_ID VARCHAR(36)    COMMENT '问题ID' ,
    USER_ID VARCHAR(36)    COMMENT '提问者' ,
    QUE_TITLE VARCHAR(36)    COMMENT '提问标题' ,
    QUE_CONTENT VARCHAR(1024)    COMMENT '提问内容' ,
